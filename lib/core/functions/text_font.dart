////////////////////////////////////////////////////font 12

TextStyle gettitleTextStyle12(
    {double? fontsize, FontWeight? fontWeight, Color? color}) {
  return TextStyle(
    fontFamily: AppFonts.fontFamily,
    fontWeight: fontWeight ?? FontWeight.bold,
    fontSize: fontsize ?? 12,
    color: color ?? AppColors.primaryColor,
  );
}

